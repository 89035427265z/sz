from fastapi import FastAPI
from fastapi.middleware.cors import CORSMiddleware
from contextlib import asynccontextmanager

from app.config import settings
from app.database import engine, Base, check_db_connection


# =============================================================================
# ЖИЗНЕННЫЙ ЦИКЛ СЕРВЕРА
# Код до yield — выполняется при старте.
# Код после yield — выполняется при остановке.
# =============================================================================
@asynccontextmanager
async def lifespan(app: FastAPI):

    # --- СТАРТ ---
    print("=" * 60)
    print(f"  KARI.Самозанятые v{settings.APP_VERSION}")
    print(f"  Режим: {'РАЗРАБОТКА (DEBUG)' if settings.DEBUG else 'ПРОДАКШН'}")
    print("=" * 60)

    # Проверяем подключение к БД
    db_ok = await check_db_connection()
    if not db_ok:
        raise RuntimeError("Не удалось подключиться к базе данных! Проверьте DATABASE_URL в .env")

    # Создаём все таблицы в БД (если ещё не созданы)
    # В продакшне таблицы создаются через Alembic (миграции), но для старта так тоже работает
    async with engine.begin() as conn:
        await conn.run_sync(Base.metadata.create_all)
    print("✅ Таблицы БД готовы")

    print(f"✅ Сервер запущен: http://localhost:8000")
    print(f"✅ Документация API: http://localhost:8000/docs")
    print("=" * 60)

    yield  # Сервер работает

    # --- ОСТАНОВКА ---
    await engine.dispose()  # Закрываем все подключения к БД
    print("Сервер остановлен.")


