# Файл: app/config.py
# =============================================================================
# Все настройки читаются из файла .env (см. .env.example)
# ВАЖНО: файл .env никогда не добавлять в git — там секретные ключи!
# =============================================================================

from pydantic_settings import BaseSettings
from typing import List


class Settings(BaseSettings):

    # -------------------------------------------------------------------------
    # БАЗА ДАННЫХ PostgreSQL
    # -------------------------------------------------------------------------
    DATABASE_URL: str = "postgresql+asyncpg://kari:password@localhost:5432/kari_samozanyatye"

    # -------------------------------------------------------------------------
    # REDIS — кэш и очереди задач (выплаты, проверка чеков ФНС)
    # -------------------------------------------------------------------------
    REDIS_URL: str = "redis://localhost:6379/0"

    # -------------------------------------------------------------------------
    # БЕЗОПАСНОСТЬ — JWT токены для авторизации
    # Сгенерировать ключ: openssl rand -hex 32
    # -------------------------------------------------------------------------
    SECRET_KEY: str = "замените-на-случайный-ключ-минимум-32-символа"
    ALGORITHM: str = "HS256"
    ACCESS_TOKEN_EXPIRE_MINUTES: int = 60 * 24  # токен живёт 24 часа

    # -------------------------------------------------------------------------
    # SMS-СЕРВИС — отправка кодов для авторизации и подписи актов (ПЭП)
    # Используем SMSC.ru или аналог
    # -------------------------------------------------------------------------
    SMS_API_URL: str = "https://smsc.ru/sys/send.php"
    SMS_API_KEY: str = ""
    SMS_SENDER: str = "KARI"
    SMS_CODE_EXPIRE_MINUTES: int = 5  # код действителен 5 минут

    # -------------------------------------------------------------------------
    # ФНС "МОЙ НАЛОГ" — интеграция для самозанятых
    # Проверка статуса, регистрация доходов, получение чеков
    # -------------------------------------------------------------------------
    FNS_API_URL: str = "https://api.nalog.ru/rnt/ais3/npd/v1"
    FNS_SOURCE_DEVICE_ID: str = ""     # ID устройства-партнёра (выдаёт ФНС)
    FNS_APP_VERSION: str = "1.0.0"
    FNS_INN_KARI: str = ""             # ИНН компании KARI (заказчик услуг)

    # -------------------------------------------------------------------------
    # СОВКОМБАНК — выплаты самозанятым
    # -------------------------------------------------------------------------
    SOVCOMBANK_API_URL: str = ""
    SOVCOMBANK_API_KEY: str = ""
    SOVCOMBANK_ACCOUNT_NUMBER: str = ""  # расчётный счёт KARI для выплат
    SOVCOMBANK_BIK: str = ""             # БИК Совкомбанка

    # -------------------------------------------------------------------------
    # MinIO — хранение фотоотчётов
    # Фото хранятся 3 года (требование ТЗ 3.10)
    # -------------------------------------------------------------------------
    MINIO_ENDPOINT: str = "localhost:9000"
    MINIO_ACCESS_KEY: str = "minioadmin"
    MINIO_SECRET_KEY: str = "minioadmin"
    MINIO_BUCKET_PHOTOS: str = "kari-photos"   # фотоотчёты к заданиям
    MINIO_BUCKET_DOCS: str = "kari-documents"  # акты и договоры
    MINIO_SECURE: bool = False                 # True когда настроим HTTPS

    # -------------------------------------------------------------------------
    # FIREBASE — push-уведомления в мобильное приложение
    # -------------------------------------------------------------------------
    FIREBASE_CREDENTIALS_PATH: str = "firebase-credentials.json"

    # -------------------------------------------------------------------------
    # ОБЩИЕ НАСТРОЙКИ
    # -------------------------------------------------------------------------
    APP_VERSION: str = "1.0.0"
    DEBUG: bool = False  # True только при разработке — не ставить True на проде!

    # Список разрешённых источников для CORS (откуда фронтенд делает запросы)
    ALLOWED_ORIGINS: List[str] = [
        "http://localhost:3000",   # React фронтенд при разработке
        "http://localhost:3001",   # Альтернативный порт
    ]

    # Лимит дохода самозанятого по закону (2 400 000 руб/год)
    SELFEMPLOYED_INCOME_LIMIT: int = 2_400_000

    # Порог риска переквалификации (если >80% дохода от KARI — предупреждение)
    INCOME_FROM_KARI_RISK_PERCENT: int = 80

    class Config:
        env_file = ".env"
        env_file_encoding = "utf-8"


# Единственный экземпляр настроек — импортируем отсюда во всех модулях
settings = Settings()
