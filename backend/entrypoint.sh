#!/bin/bash
# =============================================================================
# KARI.Самозанятые — Скрипт запуска Backend
# Запускается автоматически при старте Docker-контейнера
# =============================================================================

set -e  # Если любая команда упала — стоп

echo ""
echo "=================================================="
echo "  KARI.Самозанятые — Запуск Backend"
echo "=================================================="

# Если передана команда celery — запускаем Celery, минуя миграции
if [[ "$1" == "celery"* ]]; then
    echo "▶  Режим: Celery Worker"
    exec "$@"
fi

echo ""
echo "⏳ Ждём готовности PostgreSQL..."
# Простая пауза — healthcheck в docker-compose гарантирует что postgres готов
sleep 2

echo ""
echo "🗄️  Применяем миграции Alembic..."
alembic upgrade head

echo ""
echo "🚀 Запускаем FastAPI сервер..."
echo "   Адрес:       http://0.0.0.0:8000"
echo "   Документация: http://0.0.0.0:8000/docs"
echo ""

exec uvicorn app.main:app \
    --host 0.0.0.0 \
    --port 8000 \
    --workers 2 \
    --log-level info
