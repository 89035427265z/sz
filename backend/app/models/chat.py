#
# Зачем нужен:
#   - Исполнитель может уточнить детали задания не звоня директору
#   - Директор может дать дополнительные инструкции в процессе работы
#   - Все сообщения хранятся и видны при разборе спорных ситуаций
#   - История чата прикреплена к заданию (не теряется после завершения)
#
# =============================================================================

import uuid
from datetime import datetime

from sqlalchemy import (
    Column, String, Boolean, DateTime, Text, Index,
)
from sqlalchemy.dialects.postgresql import UUID
from sqlalchemy.sql import func

from app.database import Base


class ChatMessage(Base):
    """
    Одно сообщение в чате задания.
    Привязано к конкретному заданию — история остаётся в системе.
    """
    __tablename__ = "chat_messages"

    # -------------------------------------------------------------------------
    # ИДЕНТИФИКАЦИЯ
    # -------------------------------------------------------------------------
    id = Column(
        UUID(as_uuid=True),
        primary_key=True,
        default=uuid.uuid4,
        comment="Уникальный идентификатор сообщения",
    )

    # -------------------------------------------------------------------------
    # СВЯЗИ
    # -------------------------------------------------------------------------
    task_id = Column(
        UUID(as_uuid=True),
        nullable=False,
        index=True,
        comment="Задание в рамках которого идёт переписка",
    )
    sender_id = Column(
        UUID(as_uuid=True),
        nullable=False,
        comment="Отправитель сообщения",
    )
    receiver_id = Column(
        UUID(as_uuid=True),
        nullable=False,
        comment="Получатель сообщения",
    )

    # -------------------------------------------------------------------------
    # СОДЕРЖИМОЕ
    # -------------------------------------------------------------------------
    message = Column(
        Text,
        nullable=False,
        comment="Текст сообщения",
    )
    is_read = Column(
        Boolean,
        default=False,
        nullable=False,
        comment="Прочитано ли получателем",
    )

    # Прикреплённое фото (опционально — например фото проблемы)
    photo_url = Column(
        String(512),
        nullable=True,
        comment="URL прикреплённого фото (MinIO)",
    )

    # -------------------------------------------------------------------------
    # СЛУЖЕБНЫЕ
    # -------------------------------------------------------------------------
    created_at = Column(
        DateTime(timezone=True),
        server_default=func.now(),
        nullable=False,
    )

    # =========================================================================
    # ИНДЕКСЫ
    # =========================================================================
    __table_args__ = (
        # Загрузка сообщений по заданию в хронологическом порядке
        Index("ix_chat_task_created", "task_id", "created_at"),
        # Счётчик непрочитанных для конкретного получателя
        Index("ix_chat_receiver_unread", "receiver_id", "is_read"),
    )

    def __repr__(self) -> str:
        return (
            f"<ChatMessage task={self.task_id} "
            f"from={self.sender_id} read={self.is_read}>"
        )
