# =============================================================================
# KARI.Самозанятые — Подключение к базе данных
# Файл: app/database.py
# =============================================================================
# Используем асинхронный SQLAlchemy — это значит сервер может обслуживать
# много запросов одновременно, не застревая на ожидании ответа от БД.
# =============================================================================

from sqlalchemy.ext.asyncio import (
    create_async_engine,
    AsyncSession,
    async_sessionmaker,
)
from sqlalchemy.orm import DeclarativeBase
from sqlalchemy import text
from app.config import settings


# =============================================================================
# ДВИЖОК БАЗЫ ДАННЫХ
# Это основное подключение к PostgreSQL — создаётся один раз при старте.
# =============================================================================
engine = create_async_engine(
    settings.DATABASE_URL,
    echo=settings.DEBUG,     # При DEBUG=True выводит SQL-запросы в консоль
    pool_size=10,            # 10 постоянных подключений к БД
    max_overflow=20,         # До 20 дополнительных при большой нагрузке
    pool_pre_ping=True,      # Проверять соединение перед каждым запросом
    pool_recycle=3600,       # Пересоздавать соединение каждый час
)


# =============================================================================
# ФАБРИКА СЕССИЙ
# Каждый HTTP-запрос получает свою изолированную сессию с БД.
# После запроса сессия автоматически закрывается.
# =============================================================================
AsyncSessionLocal = async_sessionmaker(
    engine,
    class_=AsyncSession,
    expire_on_commit=False,  # Объекты остаются доступны после commit
)


# =============================================================================
# БАЗОВЫЙ КЛАСС ДЛЯ МОДЕЛЕЙ
# Все таблицы БД (User, Task, Payment и т.д.) наследуются от Base.
# =============================================================================
class Base(DeclarativeBase):
    pass


# =============================================================================
# ЗАВИСИМОСТЬ ДЛЯ FASTAPI
# Используется в обработчиках запросов: async def endpoint(db: AsyncSession = Depends(get_db))
# Автоматически делает commit при успехе или rollback при ошибке.
# =============================================================================
async def get_db():
    """Выдаёт сессию БД для одного HTTP-запроса."""
    async with AsyncSessionLocal() as session:
        try:
            yield session
            await session.commit()       # Сохраняем изменения если всё ок
        except Exception:
            await session.rollback()     # Откатываем если была ошибка
            raise
        finally:
            await session.close()        # Закрываем сессию в любом случае


# =============================================================================
# ПРОВЕРКА ПОДКЛЮЧЕНИЯ
# Вызывается при старте сервера чтобы убедиться что БД доступна.
# =============================================================================
async def check_db_connection() -> bool:
    """Проверяет доступность базы данных. Возвращает True если всё ок."""
    try:
        async with AsyncSessionLocal() as session:
            await session.execute(text("SELECT 1"))
        return True
    except Exception as e:
        print(f"❌ Ошибка подключения к БД: {e}")
        return False
