# Файл: app/models/rating.py
# =============================================================================
#
# Рейтинг исполнителя:
#   - Выставляется директором магазина после каждого принятого задания
#   - От 1 до 5 звёзд + необязательный комментарий
#   - Средний рейтинг обновляется автоматически в профиле исполнителя
#   - Исполнители с рейтингом < 3.0 получают метку «низкий рейтинг»
#
# =============================================================================

import uuid
from datetime import datetime

from sqlalchemy import (
    Column, String, Integer, Float, Text, DateTime, Index,
)
from sqlalchemy.dialects.postgresql import UUID
from sqlalchemy.sql import func

from app.database import Base


class Rating(Base):
    """
    Оценка качества работы исполнителя директором магазина.
    Одна запись = одна оценка после одного принятого задания.
    """
    __tablename__ = "ratings"

    # -------------------------------------------------------------------------
    # ИДЕНТИФИКАЦИЯ
    # -------------------------------------------------------------------------
    id = Column(
        UUID(as_uuid=True),
        primary_key=True,
        default=uuid.uuid4,
        comment="Уникальный идентификатор оценки",
    )

    # -------------------------------------------------------------------------
    # СВЯЗИ
    # -------------------------------------------------------------------------
    task_id = Column(
        UUID(as_uuid=True),
        nullable=False,
        index=True,
        comment="Задание за которое выставлена оценка",
    )
    executor_id = Column(
        UUID(as_uuid=True),
        nullable=False,
        index=True,
        comment="Исполнитель которого оценивают",
    )
    rated_by_id = Column(
        UUID(as_uuid=True),
        nullable=False,
        comment="Директор магазина, выставивший оценку",
    )

    # -------------------------------------------------------------------------
    # ОЦЕНКА
    # -------------------------------------------------------------------------
    score = Column(
        Integer,
        nullable=False,
        comment="Оценка от 1 до 5 звёзд",
    )
    comment = Column(
        Text,
        nullable=True,
        comment="Необязательный комментарий директора",
    )

    # -------------------------------------------------------------------------
    # СЛУЖЕБНЫЕ
    # -------------------------------------------------------------------------
    created_at = Column(
        DateTime(timezone=True),
        server_default=func.now(),
        nullable=False,
    )

    # =========================================================================
    # ИНДЕКСЫ
    # =========================================================================
    __table_args__ = (
        # Один исполнитель — одна оценка за одно задание
        Index("uq_rating_task_executor", "task_id", "executor_id", unique=True),
        # Быстрый расчёт среднего рейтинга исполнителя
        Index("ix_rating_executor_score", "executor_id", "score"),
    )

    def __repr__(self) -> str:
        return f"<Rating executor={self.executor_id} score={self.score}>"
