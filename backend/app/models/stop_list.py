# =============================================================================
# KARI.Самозанятые — Модель стоп-листа (422-ФЗ ст.6 п.2 пп.8)
# Файл: app/models/stop_list.py
# =============================================================================
#
# Стоп-лист защищает KARI от нарушения закона 422-ФЗ:
#
#   Статья 6, п.2, пп.8:
#   «Не признаются объектом налогообложения НПД доходы от оказания услуг
#    (работ) по ГПД при условии, что заказчиком услуг выступает работодатель
#    указанного лица или лицо, бывшее его работодателем менее двух лет назад»
#
# Кто попадает в стоп-лист:
#   1. FORMER_EMPLOYEE — бывшие сотрудники KARI (уволились < 2 лет назад)
#   2. FNS_FINE       — лица, за которых KARI получал штрафы от ФНС
#   3. MANUAL         — добавлены вручную HR/директором региона
#
# Последствия попадания в стоп-лист:
#   - Нельзя взять задание KARI с биржи
#   - Получают уведомление с причиной и двумя альтернативами:
#     a) Оформиться в штат KARI (с инструкцией)
#     b) Принять заказы внешних партнёров (другая биржа)
#
# =============================================================================

import uuid
from datetime import datetime
from enum import Enum

from sqlalchemy import (
    Column, String, Boolean, DateTime, Date, Text, Index,
)
from sqlalchemy.dialects.postgresql import UUID
from sqlalchemy import Enum as SAEnum
from sqlalchemy.sql import func

from app.database import Base


# =============================================================================
# ПЕРЕЧИСЛЕНИЯ
# =============================================================================

class StopListReason(str, Enum):
    """Причина блокировки — откуда взялась запись в стоп-листе."""
    FORMER_EMPLOYEE = "former_employee"  # Бывший сотрудник KARI (< 2 лет с увольнения)
    FNS_FINE        = "fns_fine"         # Штраф от ФНС по этому ИНН
    MANUAL          = "manual"           # Добавлен вручную (HR, директор региона)
    FISCAL_RISK     = "fiscal_risk"      # Все 3 критерия ФНС (доход + срок + доля НПД) — риск переквалификации


# =============================================================================
# ТАБЛИЦА СТОП-ЛИСТА
# =============================================================================

class StopList(Base):
    """
    Записи стоп-листа — ИНН лиц, которым запрещено получать задания от KARI.

    Проверка выполняется в API /tasks/{id}/take при каждой попытке взять задание.
    Если ИНН исполнителя найден в активных записях стоп-листа — задание не выдаётся.
    """
    __tablename__ = "stop_list"

    # -------------------------------------------------------------------------
    # ИДЕНТИФИКАЦИЯ
    # -------------------------------------------------------------------------
    id = Column(
        UUID(as_uuid=True),
        primary_key=True,
        default=uuid.uuid4,
        comment="Уникальный идентификатор записи стоп-листа",
    )

    # -------------------------------------------------------------------------
    # ДАННЫЕ ЗАБЛОКИРОВАННОГО ЛИЦА
    # -------------------------------------------------------------------------
    inn = Column(
        String(12),
        nullable=False,
        index=True,
        comment="ИНН физлица (12 цифр). Ключ для проверки.",
    )
    full_name = Column(
        String(255),
        nullable=True,
        comment="ФИО для справки оператора (не используется в проверке)",
    )

    # -------------------------------------------------------------------------
    # ПРИЧИНА БЛОКИРОВКИ
    # -------------------------------------------------------------------------
    reason = Column(
        SAEnum(StopListReason, name="stop_list_reason"),
        nullable=False,
        comment="Причина попадания в стоп-лист",
    )
    reason_details = Column(
        Text,
        nullable=True,
        comment="Подробности: номер приказа об увольнении, дата штрафа ФНС, комментарий",
    )

    # -------------------------------------------------------------------------
    # ПЕРИОД БЛОКИРОВКИ
    # -------------------------------------------------------------------------
    employment_end_date = Column(
        Date,
        nullable=True,
        comment="Дата увольнения (для FORMER_EMPLOYEE). Блокировка снимается через 2 года.",
    )
    blocked_until = Column(
        Date,
        nullable=True,
        comment=(
            "Дата окончания блокировки. NULL = бессрочно. "
            "Для FORMER_EMPLOYEE = дата_увольнения + 2 года."
        ),
    )

    # -------------------------------------------------------------------------
    # СЛУЖЕБНАЯ ИНФОРМАЦИЯ
    # -------------------------------------------------------------------------
    created_by_id = Column(
        UUID(as_uuid=True),
        nullable=True,
        comment="Кто добавил запись (NULL = автоматически при импорте)",
    )
    created_at = Column(
        DateTime(timezone=True),
        server_default=func.now(),
        nullable=False,
        comment="Дата добавления в стоп-лист",
    )
    updated_at = Column(
        DateTime(timezone=True),
        onupdate=func.now(),
        nullable=True,
        comment="Дата последнего изменения",
    )
    deactivated_at = Column(
        DateTime(timezone=True),
        nullable=True,
        comment="Дата деактивации записи (когда сняли блокировку досрочно)",
    )
    deactivated_by_id = Column(
        UUID(as_uuid=True),
        nullable=True,
        comment="Кто снял блокировку досрочно",
    )
    is_active = Column(
        Boolean,
        default=True,
        nullable=False,
        index=True,
        comment="Активна ли блокировка. False = блокировка снята.",
    )

    # =========================================================================
    # ИНДЕКСЫ
    # =========================================================================
    __table_args__ = (
        # Главный запрос: проверка ИНН в активных записях
        Index("ix_stop_list_inn_active", "inn", "is_active"),
        # Поиск по дате — для автоматического снятия истёкших блокировок
        Index("ix_stop_list_blocked_until", "blocked_until", "is_active"),
    )

    def __repr__(self) -> str:
        return (
            f"<StopList inn={self.inn} reason={self.reason} "
            f"active={self.is_active} until={self.blocked_until}>"
        )
